# Módulo 1 — Variables y tipos de datos
# Ejercicio 1: crear variables de distintos tipos

# Tipo str (string): texto entre comillas
nombre = "Emanuel"

# Tipo int (integer): número entero sin punto decimal
edad = 25

# Tipo float: número con punto decimal
altura = 1.75

# Tipo bool (boolean): solo puede ser True o False
activo = True

# Mostramos cada variable con print()
print(nombre)
print(edad)
print(altura)
print(activo)

# Verificamos el tipo de cada variable con type()
print(type(nombre))   # <class 'str'>
print(type(edad))     # <class 'int'>
print(type(altura))   # <class 'float'>
print(type(activo))   # <class 'bool'>